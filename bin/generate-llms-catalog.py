FRONTMATTER_RE = re.compile(r"^---\n(.+?)\n---", re.DOTALL)


def parse_frontmatter(file_path):
    """Extract YAML frontmatter from a markdown file."""
    text = file_path.read_text(encoding="utf-8")
    match = FRONTMATTER_RE.match(text)
    if not match:
        return None
    try:
        return yaml.safe_load(match.group(1))
    except yaml.YAMLError:
        return None


def file_to_path(file_path, content_dir, route_base, frontmatter):
    """Convert a file path to its URL path on the site."""
    slug = frontmatter.get("slug")
    rel = file_path.relative_to(content_dir)

    # Blog posts always use slug
    if route_base == "blog":
        if slug:
            return f"/blog/{slug}"
        stem = rel.parent.name if rel.name.startswith("index") else rel.stem
        return f"/blog/{stem}"

    # Docs sections
    if slug is not None:
        if slug == "/":
            return f"/{route_base}" if route_base else "/"
        if slug.startswith("/"):
            return f"/{route_base}{slug}" if route_base else slug
        return f"/{route_base}/{slug}" if route_base else f"/{slug}"

    # Index files without slug
    if rel.stem == "index":
        subdir = str(rel.parent)
        if subdir == ".":
            return f"/{route_base}" if route_base else "/"
        return f"/{route_base}/{subdir}" if route_base else f"/{subdir}"

    # Regular files
    name = str(rel.with_suffix(""))
    return f"/{route_base}/{name}" if route_base else f"/{name}"


def first_sentence(text):
    """Extract the first sentence from a description string."""
    if not text:
        return ""
    text = text.strip()
    # Look for sentence-ending punctuation followed by space or end
    match = re.match(r"(.+?[.!?])(?:\s|$)", text, re.DOTALL)
    if match:
        return match.group(1).strip()
    return text


def collect_pages(base_dir, section):
    """Collect all pages from a content section."""
    content_dir = base_dir / section["content_dir"]
    if not content_dir.is_dir():
        return []

    pages = []
    for file_path in sorted(content_dir.rglob("*")):
        if file_path.suffix not in (".md", ".mdx"):
            continue
        # Skip tag pages generated by Docusaurus
        if "tags" in file_path.parts:
            continue

        fm = parse_frontmatter(file_path)
        if fm is None:
            print(f"  warning: no frontmatter in {file_path}", file=sys.stderr)
            continue

        title = fm.get("title", file_path.stem)
        description = fm.get("description", "")
        tags = fm.get("tags") or []
        sidebar_position = fm.get("sidebar_position")
        date = fm.get("date")
        url_path = file_to_path(
            file_path, content_dir, section["route_base"], fm
        )

        page = {
            "title": title,
            "description": description,
            "path": url_path,
            "tags": tags,
            "source_file": str(file_path.relative_to(base_dir)),
        }
        if sidebar_position is not None:
            page["sidebar_position"] = sidebar_position
        if date is not None:
            page["date"] = str(date)

        pages.append(page)

    return _sort_pages(pages, section["route_base"])


def _sort_pages(pages, route_base):
    """Sort pages: index first, then by sidebar_position, then by title."""
    if route_base == "blog":
        return sorted(pages, key=lambda p: p.get("date", ""), reverse=True)

    def sort_key(page):
        is_index = page["path"].rstrip("/") == f"/{route_base}".rstrip("/")
        pos = page.get("sidebar_position")
        # Index pages first, then by sidebar_position (missing last), then title
        return (
            0 if is_index else 1,
            pos if pos is not None else 99999,
            page["title"].lower(),
        )

    return sorted(pages, key=sort_key)


def generate_llms_txt(site_url, sections_data):
    """Generate llms.txt content following the llmstxt.org standard."""
    lines = [
        f"# {SITE_NAME}",
        "",
        f"> {SITE_TAGLINE}",
        "",
    ]

    for section in sections_data:
        lines.append(f"## {section['title']}")
        lines.append("")
        lines.append(section["description"])
        lines.append("")

        for page in section["pages"]:
            url = f"{site_url}{page['path']}"
            desc = first_sentence(page["description"])
            if desc:
                lines.append(f"- [{page['title']}]({url}): {desc}")
            else:
                lines.append(f"- [{page['title']}]({url})")

        lines.append("")

    return "\n".join(lines)


def generate_catalog_json(site_url, sections_data):
    """Generate catalog.json structure."""
    total = sum(len(s["pages"]) for s in sections_data)
    catalog = {
        "site": {
            "name": SITE_NAME,
            "url": site_url,
            "tagline": SITE_TAGLINE,
        },
        "generated": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
        "total_pages": total,
        "sections": [],
    }

    for section in sections_data:
        pages_out = []
        for page in section["pages"]:
            entry = {
                "title": page["title"],
                "description": page["description"],
                "url": f"{site_url}{page['path']}",
                "path": page["path"],
                "tags": page["tags"],
                "source_file": page["source_file"],
            }
            if "sidebar_position" in page:
                entry["sidebar_position"] = page["sidebar_position"]
            if "date" in page:
                entry["date"] = page["date"]
            pages_out.append(entry)

        catalog["sections"].append(
            {
                "key": section["key"],
                "title": section["title"],
                "route_base": f"/{section['route_base']}" if section["route_base"] else "/",
                "description": section["description"],
                "page_count": len(pages_out),
                "pages": pages_out,
            }
        )

    return catalog


def main():
    parser = argparse.ArgumentParser(
        description="Generate llms.txt and catalog.json for Bactopia docs"
    )
    parser.add_argument(
        "--base-dir",
        default=".",
        help="Root directory of the docs repo (default: .)",
    )
    parser.add_argument(
        "--site-url",
        default="https://bactopia.github.io",
        help="Site base URL (default: https://bactopia.github.io)",
    )
    parser.add_argument(
        "--output-dir",
        default="static",
        help="Output directory for generated files (default: static)",
    )
    args = parser.parse_args()

    base_dir = Path(args.base_dir).resolve()
    output_dir = base_dir / args.output_dir

    sections_data = []
    for section in SECTIONS:
        print(f"Collecting {section['title']}...")
        pages = collect_pages(base_dir, section)
        sections_data.append(
            {
                "key": section["key"],
                "title": section["title"],
                "route_base": section["route_base"],
                "description": section["description"],
                "pages": pages,
            }
        )
        print(f"  {len(pages)} pages")

    site_url = args.site_url.rstrip("/")

    llms_path = output_dir / "llms.txt"
    llms_content = generate_llms_txt(site_url, sections_data)
    llms_path.write_text(llms_content, encoding="utf-8")
    print(f"\nWrote {llms_path}")

    catalog_path = output_dir / "catalog.json"
    catalog = generate_catalog_json(site_url, sections_data)
    catalog_json = json.dumps(catalog, indent=2, ensure_ascii=False) + "\n"
    catalog_path.write_text(catalog_json, encoding="utf-8")
    print(f"Wrote {catalog_path}")

    total = sum(len(s["pages"]) for s in sections_data)
    print(f"\nTotal: {total} pages across {len(sections_data)} sections")


if __name__ == "__main__":
    main()
